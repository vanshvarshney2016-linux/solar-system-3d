// ---------------------------------------------------------------------------
// Lookup helpers
// ---------------------------------------------------------------------------
export const PLANET_MAP = new Map<string, PlanetData>(
  PLANETS.map((p) => [p.id, p]),
);
export const MOON_MAP = new Map<string, MoonData>(MOONS.map((m) => [m.id, m]));

/** All body IDs in display order (sun → planets → moons) */
export const ALL_BODY_IDS: string[] = [
  "sun",
  ...PLANETS.map((p) => p.id),
  ...MOONS.map((m) => m.id),
];

/** Get moons for a given planet id */
export function getMoonsForPlanet(planetId: string): MoonData[] {
  const planet = PLANET_MAP.get(planetId);
  if (!planet?.moonIds?.length) return [];
  return planet.moonIds.flatMap((id) => MOON_MAP.get(id) ?? []);
}
